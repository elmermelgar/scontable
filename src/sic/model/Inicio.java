    private void btn_empleado_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_empleado_guardarActionPerformed
        String nombre = t_empleado_nombre.getText().trim();
        String apellido = t_empleado_apellido.getText().trim();
        Double salario = Double.parseDouble(t_empleado_salario.getText().trim());
        EmpleadoDB edb = new EmpleadoDB();
        if (actualizar_empleado) {//ACTUALIZAR LA INFORMACION DE UN EMPLEADO
            Empleado empl = empleadoTM.getEmpleados().get(tabla_empleado.getSelectedRow());
            if (edb.actualizar(empl.getId_emplado(), nombre, apellido, salario)) {
                empleadoTM.setEmpleados(edb.getEmpleados());
                scroll_tabla_empleado.setViewportView(tabla_empleado);
                tabla_empleado.repaint();
                msg_crud_empleado.setText("* El empleado fue actualizado");
            } else {
                msg_crud_empleado.setText("* No se actualizó el empleado");
            }
        } else {//GUARDAR NUEVO EMPLEADO
            if (edb.guardar(nombre, apellido, salario)) {
                empleadoTM.setEmpleados(edb.getEmpleados());
                scroll_tabla_empleado.setViewportView(tabla_empleado);
                tabla_empleado.repaint();
                msg_crud_empleado.setText("* El empleado fue almacenado");
            } else {
                msg_crud_empleado.setText("* No se registró el empleado");
            }
        }
        actualizar_empleado = false;
        t_empleado_nombre.setText(null);
        t_empleado_apellido.setText(null);
        t_empleado_salario.setText(null);
        panel_empleado_datos.setVisible(false);
    }//GEN-LAST:event_btn_empleado_guardarActionPerformed

